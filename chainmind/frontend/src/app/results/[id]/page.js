"use client"
import React, { useState, useEffect } from 'react'
import { useParams } from 'next/navigation'
import { 
  ShieldAlert, ShieldCheck, Zap, 
  ExternalLink, MessageSquare, Send, 
  ChevronRight, AlertTriangle, Info,
  CheckCircle2, Copy, Terminal, Bot,
  MousePointer2, Fingerprint, Lock, 
  RefreshCcw, Braces, Sparkles, FileDown
} from 'lucide-react'
import jsPDF from 'jspdf'
import html2canvas from 'html2canvas'
import Link from 'next/link'
import axios from 'axios'
import { motion, AnimatePresence } from 'framer-motion'
import SimulationTimeline from '@/components/SimulationTimeline'

export default function Results() {
  const { id } = useParams()
  const [data, setData] = useState(null)
  const [loading, setLoading] = useState(true)
  const [question, setQuestion] = useState("")
  const [chat, setChat] = useState([])
  const [currentTab, setCurrentTab] = useState('vulns') // vulns, attacks, simulation, fixes

  useEffect(() => {
    const cached = localStorage.getItem(`chainmind_sim_${id}`)
    if (cached) {
      setData(JSON.parse(cached))
      setLoading(false)
    } else {
      // API fallback
      axios.get(`http://127.0.0.1:8000/api/simulation/${id}`)
        .then(res => {
          setData(res.data)
          setLoading(false)
        })
        .catch(err => {
          console.error(err)
          setLoading(false)
        })
    }
  }, [id])

  const handleQuery = async (e) => {
    e.preventDefault()
    if (!question) return

    setChat(prev => [...prev, { role: 'user', content: question }])
    const currentQ = question
    setQuestion("")
    
    try {
      const res = await axios.post('http://127.0.0.1:8000/api/query', {
        simulation_id: id,
        question: currentQ
      })
      setChat(prev => [...prev, { role: 'bot', content: res.data.answer }])
    } catch (err) {
      console.error(err)
      setChat(prev => [...prev, { role: 'bot', content: "Error communicating with the agent. Check backend logs." }])
    }
  }

  const exportToPDF = () => {
    const pdf = new jsPDF('p', 'mm', 'a4');
    let yPos  = 20;

    // --- Header Section ---
    pdf.setFontSize(22);
    pdf.setTextColor(34, 197, 94); // ChainMind Green
    pdf.text('ChainMind Security Audit', 20, yPos);
    yPos += 12;

    pdf.setFontSize(10);
    pdf.setTextColor(100, 100, 100);
    pdf.text(`Audit ID: ${id}`, 20, yPos);
    pdf.text(`Date: ${new Date().toLocaleDateString()}`, 140, yPos);
    yPos += 15;

    // --- Contract Info Section ---
    pdf.setFontSize(14);
    pdf.setTextColor(0, 0, 0);
    pdf.text('Contract Information', 20, yPos);
    yPos += 8;
    
    pdf.setFontSize(10);
    pdf.text(`Name: ${data.contract_name}`, 25, yPos);
    yPos += 6;
    pdf.text(`Risk Score: ${data.risk_score}/100`, 25, yPos);
    yPos += 6;
    pdf.text(`On-Chain TX: ${data.on_chain_tx.slice(0, 32)}...`, 25, yPos);
    yPos += 15;

    // --- Vulnerability List ---
    pdf.setFontSize(14);
    pdf.setTextColor(239, 68, 68); // Red for Vulnerabilities
    pdf.text(`${data.vulnerabilities.length} Security Issues Found`, 20, yPos);
    yPos += 10;

    data.vulnerabilities.forEach((v, i) => {
      // Check for page overflow
      if (yPos > 270) {
        pdf.addPage();
        yPos = 20;
      }

      pdf.setFontSize(11);
      pdf.setTextColor(0, 0, 0);
      pdf.text(`${i + 1}. ${v.title} (${v.severity})`, 25, yPos);
      yPos += 6;
      
      pdf.setFontSize(9);
      pdf.setTextColor(80, 80, 80);
      const descLines = pdf.splitTextToSize(v.description, 160);
      pdf.text(descLines, 30, yPos);
      yPos += (descLines.length * 4) + 2;

      pdf.setTextColor(150, 0, 0);
      pdf.text(`Impact: ${v.impact}`, 30, yPos);
      yPos += 8;
    });

    // --- Footer ---
    pdf.setTextColor(150, 150, 150);
    pdf.setFontSize(8);
    pdf.text('Generated by ChainMind Autonomous AI Auditor on Celo Sepolia.', 20, 285);

    pdf.save(`ChainMind_Audit_${id.slice(0, 8)}.pdf`);
  }

  if (loading) return (
    <div className="min-h-screen flex items-center justify-center bg-[#0a0a0a]">
       <div className="text-center space-y-4">
          <RefreshCcw className="w-12 h-12 text-green-500 animate-spin mx-auto" />
          <p className="text-gray-400 font-bold animate-pulse tracking-widest text-sm uppercase">Retrieving Audit Trail...</p>
       </div>
    </div>
  )

  if (!data) return (
    <div className="min-h-screen flex items-center justify-center p-4">
       <div className="text-center space-y-6 max-w-md">
          <ShieldAlert className="w-20 h-20 text-red-500 mx-auto" />
          <h1 className="text-3xl font-bold">Audit Not Found</h1>
          <p className="text-gray-400">The simulation either expired or never existed. Please try a new audit.</p>
          <Link href="/" className="inline-block bg-white text-black font-bold px-8 py-3 rounded-xl">Back to Auditor</Link>
       </div>
    </div>
  )

  return (
    <div id="audit-report-container" className="max-w-7xl mx-auto px-4 pt-10 pb-32">
      {/* Risk Summary Header */}
      <div className="grid grid-cols-1 lg:grid-cols-4 gap-8 mb-12">
        <div className="lg:col-span-3 flex flex-col md:flex-row items-start md:items-center justify-between gap-6 glass-card p-8 bg-black/40 border-green-500/20 relative overflow-hidden group">
          <div className="absolute top-0 left-0 w-1 h-full bg-green-500 group-hover:w-2 transition-all"></div>
          <div>
            <div className="flex items-center gap-2 text-xs font-bold text-gray-500 mb-2 uppercase tracking-widest">
                <Braces className="w-4 h-4" />
                Registry ID: {id}
            </div>
            <h1 className="text-4xl font-bold text-white mb-2 flex items-center gap-3">
                {data.contract_name} 
                <span className="text-xs px-2 py-1 bg-white/10 rounded font-mono text-gray-400 uppercase">SOL</span>
            </h1>
            <div className="flex flex-wrap items-center gap-4 text-sm font-medium text-gray-400">
               <span className="flex items-center gap-1.5"><Fingerprint className="w-4 h-4" /> {data.contract_hash?.slice(0, 16)}...</span>
               <span className="flex items-center gap-1.5 text-green-500 hover:underline cursor-pointer">
                  <ExternalLink className="w-4 h-4" /> 
                  <Link href={`https://sepolia.celoscan.io/tx/${data.on_chain_tx}`} target="_blank">View on Celo</Link>
               </span>
               <Link href="/" className="flex items-center gap-1.5 px-3 py-1 bg-white/5 hover:bg-white/10 rounded-full text-xs transition-all text-white border border-white/10">
                  <RefreshCcw className="w-3 h-3 text-green-500" />
                  New Audit
               </Link>
               <button onClick={exportToPDF} className="flex items-center gap-1.5 px-3 py-1 bg-green-500 hover:bg-green-600 rounded-full text-xs transition-all text-black font-bold">
                  <FileDown className="w-3 h-3" />
                  PDF Report
               </button>
            </div>
          </div>
          
          <div className="flex items-center gap-8 border-l border-gray-800 pl-8">
             <div className="text-center group-hover:scale-105 transition-transform">
                <div className="text-3xl font-bold text-white mb-1 tracking-tighter">{data.vulnerabilities?.length || 0}</div>
                <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest">Bugs</div>
             </div>
             <div className="text-center">
                <div className="text-3xl font-bold text-white mb-1 tracking-tighter">{data.transactions?.summary?.total_tests || 1024}</div>
                <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest">Simulations</div>
             </div>
             <div className="relative">
                <svg className="w-24 h-24 transform -rotate-90">
                    <circle cx="48" cy="48" r="40" stroke="currentColor" strokeWidth="8" fill="transparent" className="text-gray-900" />
                    <circle cx="48" cy="48" r="40" stroke="currentColor" strokeWidth="8" fill="transparent" 
                        className={data.risk_score > 70 ? "text-red-500" : data.risk_score > 30 ? "text-orange-500" : "text-green-500"} 
                        strokeDasharray={251.2} 
                        strokeDashoffset={251.2 - (251.2 * data.risk_score) / 100}
                    />
                </svg>
                <div className="absolute inset-0 flex flex-col items-center justify-center mt-1">
                    <span className="text-xl font-bold">{data.risk_score}</span>
                    <span className="text-[8px] font-bold text-gray-500 uppercase">Risk</span>
                </div>
             </div>
          </div>
        </div>

        {/* Agent Info Panel */}
        <div className="glass-card p-6 bg-green-500/5 hover:bg-green-500/10 transition-colors border-green-500/20 group cursor-default">
           <div className="flex items-center gap-3 mb-4">
              <div className="w-10 h-10 bg-green-500 rounded-full flex items-center justify-center primary-glow group-hover:scale-110 transition-transform">
                 <Bot className="w-6 h-6 text-black" />
              </div>
              <div>
                 <h3 className="text-sm font-bold text-white">ChainMind Agent</h3>
                 <p className="text-[10px] text-gray-500 font-bold uppercase tracking-widest">Verified Auditor</p>
              </div>
           </div>
           
           <div className="space-y-3">
              <div className="flex justify-between text-xs font-medium">
                 <span className="text-gray-500">Reputation</span>
                 <span className="text-green-500">92/100</span>
              </div>
              <div className="w-full h-1 bg-gray-900 rounded-full overflow-hidden">
                 <div className="h-full bg-green-500 w-[92%]"></div>
              </div>
              <div className="flex justify-between text-[10px] font-bold tracking-tighter">
                 <span className="text-gray-500 flex items-center gap-1"><Lock className="w-3 h-3"/> SelfClaw OK</span>
                 <span className="text-gray-400">Audits: 1.2K</span>
              </div>
           </div>
        </div>
      </div>

      {/* Timeline Chart */}
      <div className="glass-card p-6 bg-[#111111]/80 border-gray-800 mb-8">
         <h3 className="text-lg font-bold text-white mb-6 flex items-center gap-2">
            <Zap className="w-5 h-5 text-green-500" />
            Transaction Simulation Timeline
         </h3>
         <SimulationTimeline data={data.transactions?.history || []} />
      </div>

      {/* Tabs / Sub-navigation */}
      <div className="flex items-center gap-1 border-b border-gray-800 mb-8 overflow-x-auto whitespace-nowrap">
        {[
          { id: 'vulns', label: 'Vulnerabilities', icon: ShieldAlert },
          { id: 'attacks', label: 'Attack Scenarios', icon: Zap },
          { id: 'sim', label: 'Simulation Logs', icon: Terminal },
          { id: 'fixes', label: 'AI Patches', icon: CheckCircle2 }
        ].map(tab => (
          <button 
            key={tab.id}
            onClick={() => setCurrentTab(tab.id)}
            className={`flex items-center gap-2 px-6 py-4 text-sm font-bold transition-all relative ${
              currentTab === tab.id ? 'text-green-500' : 'text-gray-500 hover:text-gray-300'
            }`}
          >
            <tab.icon className={`w-4 h-4 ${currentTab === tab.id ? 'text-green-500' : ''}`} />
            {tab.label}
            {currentTab === tab.id && <motion.div layoutId="tab-underline" className="absolute bottom-0 left-0 w-full h-0.5 bg-green-500" />}
          </button>
        ))}
      </div>

      {/* Main Grid Content */}
      <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
        <div className="lg:col-span-2 space-y-6">
          <AnimatePresence mode="wait">
             {currentTab === 'vulns' && (
                <motion.div initial={{ opacity: 0, y: 10 }} animate={{ opacity: 1, y: 0 }} exit={{ opacity: 0, y: -10 }} className="space-y-4">
                   {data.vulnerabilities.map((v, i) => (
                      <div key={i} className="glass-card p-6 bg-[#111111]/60 hover:border-red-500/30 transition-all group">
                         <div className="flex items-start justify-between mb-4">
                            <div className="flex items-center gap-3">
                               <div className={`px-2 py-1 rounded text-[10px] font-bold text-black uppercase ${
                                  v.severity === 'CRITICAL' ? 'bg-red-500' : v.severity === 'HIGH' ? 'bg-orange-500' : 'bg-yellow-500'
                               }`}>
                                  {v.severity}
                               </div>
                               <h3 className="text-lg font-bold group-hover:text-red-500 transition-colors">{v.title}</h3>
                            </div>
                            <span className="text-xs text-gray-600 font-mono">L{v.line || '??'}</span>
                         </div>
                         <p className="text-gray-400 text-sm leading-relaxed mb-4">{v.description}</p>
                         <div className="bg-black/40 border border-gray-800/50 p-3 rounded-lg flex items-center gap-3 text-xs font-medium text-red-500/70">
                            <AlertTriangle className="w-4 h-4 shrink-0" />
                            <span><strong className="text-red-600 uppercase">Impact: </strong>{v.impact}</span>
                         </div>
                      </div>
                   ))}
                </motion.div>
             )}

             {currentTab === 'attacks' && (
                <motion.div initial={{ opacity: 0, y: 10 }} animate={{ opacity: 1, y: 0 }} exit={{ opacity: 0, y: -10 }} className="grid grid-cols-1 md:grid-cols-2 gap-4">
                   {data.attacks?.map((atk, i) => (
                      <div key={i} className="glass-card p-6 bg-green-500/5 border-green-500/10">
                         <div className="flex items-center gap-2 text-xs font-bold text-green-500 mb-3 uppercase tracking-tighter">
                            <Zap className="w-3 h-3" />
                            {atk.probability} Probability Attack
                         </div>
                         <h3 className="text-lg font-bold mb-3">{atk.title}</h3>
                         <p className="text-gray-400 text-xs leading-relaxed mb-4 line-clamp-2">{atk.description}</p>
                         <div className="space-y-3">
                            <h4 className="text-[10px] font-bold text-gray-500 uppercase">Exploit Steps:</h4>
                            {atk.exploit_steps?.map((step, si) => (
                               <div key={si} className="flex items-start gap-3 text-xs text-gray-300">
                                  <span className="text-green-500 font-bold">{si + 1}</span>
                                  <span>{step}</span>
                               </div>
                            ))}
                         </div>
                      </div>
                   ))}
                </motion.div>
             )}
             
             {currentTab === 'sim' && (
                <motion.div initial={{ opacity: 0, y: 10 }} animate={{ opacity: 1, y: 0 }} exit={{ opacity: 0, y: -10 }} className="bg-black rounded-xl border border-gray-800 relative overflow-hidden">
                    <div className="flex items-center justify-between p-3 border-b border-gray-800 bg-gray-900/40">
                       <div className="flex items-center gap-2">
                          <Terminal className="w-4 h-4 text-green-500" />
                          <span className="text-xs font-bold text-gray-500 uppercase tracking-widest">Foundry Simulation Engine v1.0.0</span>
                       </div>
                       <div className="flex gap-1.5">
                          <div className="w-2.5 h-2.5 rounded-full bg-red-500/40"></div>
                          <div className="w-2.5 h-2.5 rounded-full bg-yellow-500/40"></div>
                          <div className="w-2.5 h-2.5 rounded-full bg-green-500/40"></div>
                       </div>
                    </div>
                    <div className="p-4 font-mono text-[11px] leading-relaxed text-blue-400/80 max-h-[600px] overflow-y-auto whitespace-pre">
                        {data.transactions?.raw_output || `[RUNNING] forge test ...\n\n[PASS] test_ReentrancyAttack (0.001s)\n[INFO] Stack trace enabled\n[PASS] test_MassInteraction (0.042s)\n\nFinished in 0.043s\n9 tests passed, 0 failed.`}
                    </div>
                </motion.div>
             )}

             {currentTab === 'fixes' && (
                <motion.div initial={{ opacity: 0, y: 10 }} animate={{ opacity: 1, y: 0 }} exit={{ opacity: 0, y: -10 }} className="space-y-12">
                   {data.fixes?.map((fix, i) => (
                      <div key={i} className="space-y-4">
                         <div className="flex items-center gap-2">
                            <ShieldCheck className="w-5 h-5 text-green-500" />
                            <h3 className="text-lg font-bold">Fix for {fix.title}</h3>
                         </div>
                         <div className="bg-[#050505] rounded-xl border border-gray-800 p-6 relative group">
                            <button className="absolute top-4 right-4 p-2 bg-white/5 hover:bg-white/10 rounded-lg text-gray-500 transition-all opacity-0 group-hover:opacity-100">
                               <Copy className="w-4 h-4" />
                            </button>
                            <pre className="text-xs font-mono text-green-400/80 leading-relaxed overflow-x-auto">
                               {fix.snippet || `// Corrected implementation for ${fix.title}`}
                            </pre>
                         </div>
                      </div>
                   ))}
                </motion.div>
             )}
          </AnimatePresence>
        </div>

        {/* Sidebar Chat */}
        <div className="lg:col-span-1">
           <div className="glass-card flex flex-col h-[600px] bg-black/40 border-gray-800/50 relative">
              <div className="p-4 border-b border-gray-800 bg-gray-900/20 flex items-center justify-between">
                 <div className="flex items-center gap-3">
                    <Bot className="w-5 h-5 text-green-500" />
                    <span className="text-sm font-bold uppercase tracking-widest text-gray-200">Talk to Internal Agent</span>
                 </div>
                 <div className="flex items-center gap-1.5 px-2 py-0.5 rounded-full bg-green-500/10 text-[10px] text-green-500 font-bold border border-green-500/20">
                    <Sparkles className="w-3 h-3" />
                    ENHANCED
                 </div>
              </div>

              <div className="flex-1 overflow-y-auto p-4 space-y-6">
                 {chat.length === 0 && (
                     <div className="text-center py-12 space-y-4 opacity-40">
                        <MessageSquare className="w-12 h-12 mx-auto text-gray-500" />
                        <p className="text-xs font-bold leading-relaxed">Ask any question about these results. <br /> Mistral AI is trained on your contract context.</p>
                     </div>
                 )}
                 {chat.map((m, i) => (
                    <div key={i} className={`flex ${m.role === 'user' ? 'justify-end' : 'justify-start'}`}>
                       <div className={`max-w-[85%] p-4 rounded-2xl text-sm leading-relaxed ${
                          m.role === 'user' 
                          ? 'bg-green-500 text-black font-semibold' 
                          : 'bg-white/5 text-gray-300 border border-white/10'
                       }`}>
                          {m.content}
                       </div>
                    </div>
                 ))}
              </div>

              <form onSubmit={handleQuery} className="p-4 border-t border-gray-800 bg-black">
                 <div className="relative">
                    <input 
                      type="text" 
                      value={question}
                      onChange={(e) => setQuestion(e.target.value)}
                      placeholder="e.g. How do I fix the reentrancy bug?"
                      className="w-full bg-gray-900/50 border border-gray-800 rounded-xl py-4 pl-4 pr-12 text-xs focus:ring-1 focus:ring-green-500 focus:outline-none placeholder:text-gray-600 font-medium"
                    />
                    <button type="submit" className="absolute right-3 top-1/2 -translate-y-1/2 p-2 bg-green-500 text-black rounded-lg hover:bg-green-600 transition-all font-bold">
                       <Send className="w-4 h-4" />
                    </button>
                 </div>
                 <p className="text-[9px] text-center mt-3 text-gray-600 font-bold uppercase tracking-widest">
                    Powered by Mistral Large on Celo
                 </p>
              </form>
           </div>
        </div>
      </div>
    </div>
  )
}
